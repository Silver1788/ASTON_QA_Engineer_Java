public class LectureNine {

    /**
     * 1. Создать массив с набором слов (10-20 слов, должны встречаться повторяющиеся). Найти и вывести список уникальных слов, из
     * которых состоит массив (дубликаты не считаем). Посчитать, сколько раз встречается каждое слово. (реализовать с использованием коллекций)
     *
     * 2. Написать простой класс Телефонный Справочник, который хранит в
     * себе список фамилий и телефонных номеров. В этот телефонный
     * справочник с помощью метода add() можно добавлять записи, а с помощью метода get() искать номер телефона по фамилии.
     * Следует учесть, что под одной фамилией может быть несколько телефонов (в случае однофамильцев),
     * тогда при запросе такой фамилии должны выводиться все телефоны.
     */
    public static void main(String[] args) {
        // Работа с уникальными словами
        String[] words = {
                "яблоко", "банан", "апельсин", "яблоко", "груша", "банан",
                "персик", "ананас", "апельсин", "яблоко", "слива", "персик"
        };

        UniqueWordsCounter wordCounter = new UniqueWordsCounter(words);

        // Вывод уникальных слов
        System.out.println("Уникальные слова: " + wordCounter.countWords().keySet());

        // Вывод количества повторений каждого слова
        System.out.println("Частота слов:");
        for (var entry : wordCounter.countWords().entrySet()) {
            System.out.println(entry.getKey() + ": " + entry.getValue());
        }

        // Работа с телефонным справочником
        PhoneBook phoneBook = new PhoneBook();
        phoneBook.add("Иванов", "12-34-56");
        phoneBook.add("Петров", "78-90-12");
        phoneBook.add("Иванов", "34-56-78"); // Добавление ещё одного номера для Иванова
        phoneBook.add("Сидоров", "90-12-34");

        // Вывод данных из телефонного справочника
        System.out.println("\nТелефонный справочник:");
        System.out.println("Иванов: " + phoneBook.get("Иванов"));
        System.out.println("Петров: " + phoneBook.get("Петров"));
        System.out.println("Сидоров: " + phoneBook.get("Сидоров"));
        System.out.println("Смирнов: " + phoneBook.get("Смирнов")); // У Смирнова нет записей, должен вернуться пустой список
    }
}
